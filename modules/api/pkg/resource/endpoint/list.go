package endpoint

import (
	"github.com/samber/lo"
	discoveryv1 "k8s.io/api/discovery/v1"

	"github.com/pluralsh/kubernetes-agent/types"
)

type EndpointList struct {
	ListMeta types.ListMeta `json:"listMeta"`
	// List of endpoints
	Endpoints []Endpoint `json:"endpoints"`
}

// toEndpointList converts array of api events to endpoint List structure
func toEndpointList(endpoints []discoveryv1.EndpointSlice) *EndpointList {
	endpointList := EndpointList{
		Endpoints: make([]Endpoint, 0),
		ListMeta:  types.ListMeta{TotalItems: len(endpoints)},
	}

	for _, endpoint := range endpoints {
		for _, subSets := range endpoint.Endpoints {
			for _, address := range subSets.Addresses {
				endpointList.Endpoints = append(endpointList.Endpoints, Endpoint{
					TypeMeta: types.NewTypeMeta(types.ResourceKindEndpoint),
					Host:     address,
					Ports:    endpoint.Ports,
					Ready:    lo.FromPtr(subSets.Conditions.Ready),
					NodeName: subSets.NodeName,
				})
			}
		}
	}

	return &endpointList
}
